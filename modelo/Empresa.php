<?php

class Empresa 
{
   private $codigo;
   private $empresa;   
   private $periodo;   
   
    public function __construct($codigo,$empresa,$periodo) //funcion que se autoejecuta cuando defines un objeto, le puedes poner argumentos de inicializaciÃ³n, por defecto todo es vacio
    {
     $this->codigo=$codigo;
     $this->empresa=$empresa;
     $this->periodo=$periodo;
    }
   
   public function getJsonData(){
        $var = get_object_vars($this);
        foreach($var as &$value){
           if(is_object($value) && method_exists($value,'getJsonData')){
              $value = $value->getJsonData();
           }
        }
        return $var;
   }
}

