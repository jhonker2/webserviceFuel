<?php

class Marca {
     private $codigo;
   private $marca;   
   
    public function __construct($codigo,$marca) //funcion que se autoejecuta cuando defines un objeto, le puedes poner argumentos de inicializaciÃ³n, por defecto todo es vacio
    {
     $this->codigo=$codigo;
     $this->marca=$marca;
    }
   
   public function getJsonData(){
        $var = get_object_vars($this);
        foreach($var as &$value){
           if(is_object($value) && method_exists($value,'getJsonData')){
              $value = $value->getJsonData();
           }
        }
        return $var;
   }
}
