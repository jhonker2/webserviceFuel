<?php

class Parametro {
    
  private $codigo;
  private $parametro;   
   
    public function __construct($codigo,$parametro) //funcion que se autoejecuta cuando defines un objeto, le puedes poner argumentos de inicializaciÃ³n, por defecto todo es vacio
    {
     $this->codigo=$codigo;
     $this->parametro=$parametro;
    }
   
   public function getJsonData(){
        $var = get_object_vars($this);
        foreach($var as &$value){
           if(is_object($value) && method_exists($value,'getJsonData')){
              $value = $value->getJsonData();
           }
        }
        return $var;
   }
}
